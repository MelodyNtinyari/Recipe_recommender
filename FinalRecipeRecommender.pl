recipe(turkey_and_Vegetable_StirFry, 'Ingredients:

1 pound lean ground turkey
2 cups mixed vegetables (such as bell peppers, broccoli, carrots, and snap peas)
2 tablespoons low-sodium soy sauce
1 tablespoon sesame oil
1 tablespoon grated ginger
2 cloves garlic, minced
2 green onions, sliced
1 tablespoon olive oil
Cooked brown rice or quinoa (optional)

Instructions:

Heat olive oil in a large skillet over medium-high heat. Add ground turkey and cook until browned.
Add mixed vegetables, ginger, garlic, and green onions to the skillet. Cook until vegetables are tender-crisp.
In a small bowl, mix soy sauce and sesame oil. Pour over the turkey and vegetable mixture.
Cook for an additional 2-3 minutes, stirring occasionally.
Serve the stir-fry hot over cooked brown rice or quinoa if desired.').

recipe(baked_Salmon_with_Lemon_and_Dill, 'Ingredients:

4 salmon fillets
2 tablespoons olive oil
2 tablespoons fresh lemon juice
1 tablespoon fresh dill, chopped
1 teaspoon garlic powder
Salt and pepper to taste

Instructions:

Preheat the oven to 375°F (190°C).
Place salmon fillets on a baking sheet. In a bowl, mix olive oil, lemon juice, dill, garlic powder, salt, and pepper. Brush the mixture over the salmon.
Bake salmon for 15-20 minutes or until it flakes easily with a fork.
Serve the baked salmon with a side of steamed vegetables or a salad.').

recipe(vegetable_and_Chickpea_Curry, 'Ingredients:

1 tablespoon olive oil
1 onion, diced
2 cloves garlic, minced
1 tablespoon curry powder
1 teaspoon ground cumin
1 teaspoon ground turmeric
1 can (15 ounces) chickpeas, drained and rinsed
1 can (14 ounces) diced tomatoes
2 cups mixed vegetables (such as bell peppers, carrots, and spinach)
Salt and pepper to taste
Cooked brown rice for serving

Instructions:

Heat olive oil in a large skillet over medium heat. Add onion and garlic, and cook until softened.
Stir in curry powder, cumin, and turmeric, and cook for 1 minute.
