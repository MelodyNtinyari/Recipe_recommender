recipe(gluten_Free_Chicken_Alfredo_Pasta, 'Ingredients:

8 ounces gluten-free pasta (such as brown rice or quinoa pasta)
2 tablespoons olive oil
2 boneless, skinless chicken breasts, cut into strips
2 cloves garlic, minced
1 cup broccoli florets
1 cup sliced mushrooms
1 cup gluten-free chicken broth
1 cup heavy cream
1/2 cup grated Parmesan cheese
Salt and pepper to taste
Chopped parsley for garnish

Instructions:

Cook gluten-free pasta according to package instructions. Drain and set aside.
Heat olive oil in a large skillet over medium heat. Add chicken strips and cook until browned and cooked through. Remove from skillet and set aside.
In the same skillet, add garlic, broccoli, and mushrooms. Cook until vegetables are tender.
Pour in chicken broth and heavy cream. Bring to a simmer and let it cook for 5 minutes.
Stir in cooked pasta, cooked chicken, and grated Parmesan cheese. Cook for an additional 2-3 minutes until heated through and sauce has thickened.
Season with salt and pepper to taste.
Serve hot, garnished with chopped parsley.').

